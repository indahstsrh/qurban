<div style="text-align:justify"><h5>

Sesuatu yang perlu diperhatikan bagi umat Islam adalah bahwa berqurban (udhiyah), qurban (taqarrub) dan berkorban (tadhiyah), ketiganya memiliki titik persamaan dan perbedaan. Qurban (taqarrub), yaitu upaya seorang muslim melakukan pendekatan diri kepada Allah dengan amal ibadah baik yang diwajibkan maupun yang disunnahkan. Rasulullah SAW bersabda: 

Sesungguhnya Allah berfirman (dalam hadits Qudsi): “Siapa yang memerangi kekasih-Ku, niscaya aku telah umumkan perang padanya. Tidaklah seorang hamba mendekatkan diri pada-Ku (taqarrub) dengan sesuatu yang paling Aku cintai, dengan sesuatu yang aku wajibkan. Dan jika hamba-Ku senantiasa mendekatkan diri kepada-Ku dengan yang sunnah, maka Aku mencintainya. Jika Aku telah mencintainya, maka Aku menjadi pendengarannya dimana ia mendengar, menjadi penglihatannya dimana ia melihat, tangannya dimana ia memukul dan kakinya, dimana ia berjalan. Jika ia meminta, niscaya Aku beri dan jika ia minta perlindungan, maka Aku lindungi” (HR Bukhari). 

Berqurban (udhiyah) adalah salah satu bentuk pendekatan diri kepada Allah dengan mengorbankan sebagian kecil hartanya, untuk dibelikan binatang ternak. Menyembelih binatang tersebut dengan persyaratan yang sudah ditentukan. Sedangkan berkorban (tadhiyah) mempunyai arti yang lebih luas yaitu berkorban dengan harta, jiwa, pikiran dan apa saja untuk tegaknya Islam. Dalam suasana dimana umat Islam di Indonesia sedang terkena musibah banjir, dan mereka banyak yang menjadi korban. Maka musibah ini harus menjadi pelajaran berarti bagi umat Islam. Apakah musibah ini disebabkan karena mereka menjauhi Allah SWT dan menjauhi ajaran-Nya? Yang pasti, musibah ini harus lebih mendekatkan umat Islam kepada Allah (taqqarub ilallah). Melaksanakan perintah-Nya dan menjauhi larangan-Nya. Dan yang tidak tertimpa musibah banjir ini dituntut untuk memberikan kepeduliannya dengan cara berkorban dan memberikan bantuan kepada mereka yang terkena musibah. Dan di antara bentuk pendekatan diri kepada Allah dan bentuk pengorbanan kita dengan melakukan qurban penyembelihan sapi dan kambing pada hari Raya ‘Idul Adha dan Hari Tasyrik. Semoga Allah menerima qurban kita dan meringankan musibah ini, dan yang lebih penting lagi menyelamatkan kita dari api neraka. </p></h5>
